//有一对刚出生后的兔子，从第三个月起每个月都生一对兔子
//每对兔子长大后也每个月生一对兔子
//假设兔子不死，求30个月内兔子对数


#include <stdio.h>
int main()
{
	int m1 = 1, m2 = 1;
	int i, count = 1;
	printf("%12d%12d", m1, m2);		//打印1、2月
	for (i = 0; i <= 13; i++)		//每次打印两个月，剩余14次
	{
		m1 = m1 + m2;				//从第三个月开始，每个月等于前两个月相加
		m2 = m1 + m2;				
		printf("%12d%12d", m1, m2);
		count += 1;
		if (count % 3 == 0)			//一行打印6个月
			printf("\n");
	}
}